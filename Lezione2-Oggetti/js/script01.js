var arr2 = Math.floor(8.99999999);
console.log(arr2);

var rou = Math.round(8.99999); //arrotonda normalmento
console.log(rou);

var rou2 = Math.round(1.11111);
console.log(rou2);

//Taglio - Trunc toglie le cifre decimali
